let numero = parseInt(prompt("Digite um número:"))

if (numero >= 0) {
    let = resultadoFinal = numero * 2;
    console.log(`O número ${numero} é positivo, então seu valor foi duplicado: ${resultadoFinal}.`)

} else if(numero < 0) {
    let = resultadoFinal = numero * 3;
    console.log(`O número ${numero} é negativo, então seu valor vai triplicar: ${resultadoFinal}.`)
}
